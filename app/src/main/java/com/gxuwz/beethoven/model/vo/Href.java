package com.gxuwz.beethoven.model.vo;

public class Href {

    String href;

    public String getHref() {
        return href;
    }

    public void setHref(String href) {
        this.href = href;
    }

    @Override
    public String toString() {
        return "Href{" +
                "href='" + href + '\'' +
                '}';
    }
}
