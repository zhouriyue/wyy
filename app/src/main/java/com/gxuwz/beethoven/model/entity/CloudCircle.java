package com.gxuwz.beethoven.model.entity;

public class CloudCircle {
    private String cloudCircleDiagonal;
    private double playNumber;
    private String title;
    private String subTitle;

    public CloudCircle() {
    }

    public String getCloudCircleDiagonal() {
        return cloudCircleDiagonal;
    }

    public void setCloudCircleDiagonal(String cloudCircleDiagonal) {
        this.cloudCircleDiagonal = cloudCircleDiagonal;
    }

    public double getPlayNumber() {
        return playNumber;
    }

    public void setPlayNumber(double playNumber) {
        this.playNumber = playNumber;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public void setSubTitle(String subTitle) {
        this.subTitle = subTitle;
    }
}
