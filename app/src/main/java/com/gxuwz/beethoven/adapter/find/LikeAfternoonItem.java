package com.gxuwz.beethoven.adapter.find;

public class LikeAfternoonItem {
    private String diagonal;
    private Integer playNumber;
    private String englishTitle;
    private String chinaTitle;
    private String title;

    public LikeAfternoonItem() {
    }

    public String getDiagonal() {
        return diagonal;
    }

    public void setDiagonal(String diagonal) {
        this.diagonal = diagonal;
    }

    public Integer getPlayNumber() {
        return playNumber;
    }

    public void setPlayNumber(Integer playNumber) {
        this.playNumber = playNumber;
    }

    public String getEnglishTitle() {
        return englishTitle;
    }

    public void setEnglishTitle(String englishTitle) {
        this.englishTitle = englishTitle;
    }

    public String getChinaTitle() {
        return chinaTitle;
    }

    public void setChinaTitle(String chinaTitle) {
        this.chinaTitle = chinaTitle;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
